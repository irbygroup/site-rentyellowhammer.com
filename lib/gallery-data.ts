interface GalleryImage {
  src: string;
  alt: string;
  width: number;
  height: number;
}

function generateImages(
  dir: string,
  venueName: string,
  filenames: string[],
): GalleryImage[] {
  return filenames.map((f) => ({
    src: `/images/${dir}/${f}`,
    alt: `${venueName} - ${f.replace(/[-_]/g, " ").replace(/\.\w+$/, "")}`,
    width: 800,
    height: 600,
  }));
}

